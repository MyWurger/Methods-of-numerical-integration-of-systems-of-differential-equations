// Назначение файла:
// 1) Создать объект QApplication, который запускает Qt-приложение.
// 2) Подготовить список доступных численных интеграторов.
// 3) Создать и показать главное окно MainWindow.
// 4) Передать управление в главный цикл обработки событий Qt.
// ============================================================================

// Подключаем главное окно приложения.
#include "MainWindow.h"
// Подключаем конкретную реализацию метода Эйлера.
#include "TEuler.h"
// Подключаем конкретную реализацию метода Рунге-Кутты 4 порядка.
#include "TRungeKutta.h"

// QApplication — базовый объект любого Qt Widgets-приложения.
#include <QApplication>
// QFont нужен для настройки общего шрифта интерфейса.
#include <QFont>

// std::vector нужен для хранения списка доступных методов.
#include <vector>

// ----------------------------------------------------------------------------
// ФУНКЦИЯ main
// ----------------------------------------------------------------------------
// Назначение:
// Выполнить начальную инициализацию приложения и запустить GUI.
//
// Принимает:
// - argc : число аргументов командной строки;
// - argv : массив аргументов командной строки.
//
// Возвращает:
// - код завершения приложения, который возвращает Qt event loop.
// ----------------------------------------------------------------------------
int main(int argc, char* argv[])
{
    // QApplication создается самым первым, потому что без него нельзя
    // создавать и показывать Qt-виджеты.
    QApplication app(argc, argv);

    // Задаем читаемое имя приложения.
    app.setApplicationName("Моделирование движения КА");

    // Имя организации используется Qt в ряде служебных сценариев, например
    // при хранении настроек.
    app.setOrganizationName("ModelingMethodsLab1");

    // Подготавливаем общий шрифт интерфейса.
    QFont uiFont("Avenir Next", 11);

    // PreferAntialias просит Qt использовать более качественное сглаживание
    // текста, если это возможно на текущей платформе.
    uiFont.setStyleStrategy(QFont::PreferAntialias);

    // Устанавливаем этот шрифт приложению целиком, чтобы интерфейс выглядел
    // единообразно.
    app.setFont(uiFont);

    // Создаем список доступных методов интегрирования, который затем будет
    // передан в главное окно.
    std::vector<IntegratorOption> integratorOptions;

    // Добавляем в список метод Эйлера.
    integratorOptions.push_back(
        {"Метод Эйлера",
         [](double t0, double tk, double h)
         {
             // Лямбда-функция здесь играет роль фабрики.
             //
             // Она получает параметры интегрирования и возвращает уже готовый
             // объект конкретного интегратора.
             return new TEuler(t0, tk, h);
         }});

    // Добавляем метод Рунге-Кутты 4 порядка.
    integratorOptions.push_back(
        {"Метод Рунге-Кутты 4 порядка",
         [](double t0, double tk, double h)
         {
             // Здесь используется тот же подход, только создается уже объект
             // TRungeKutta.
             return new TRungeKutta(t0, tk, h);
         }});

    // Создаем главное окно и передаем ему список всех доступных методов.
    MainWindow window(std::move(integratorOptions));

    // Показываем окно на экране.
    window.show();

    // app.exec() запускает главный цикл обработки событий Qt.
    //
    // После этого приложение начинает:
    // - реагировать на нажатия кнопок;
    // - обновлять интерфейс;
    // - обрабатывать мышь, клавиатуру и таймеры.
    return app.exec();
}
