// ============================================================================
// ФАЙЛ SIMULATIONTYPES.H - ОБЩИЕ ТИПЫ РЕЗУЛЬТАТА МОДЕЛИРОВАНИЯ
// ============================================================================
// Назначение файла:
// 1) Объявить структуры, которые описывают одну точку траектории и весь
//    результат моделирования целиком.
// 2) Отделить общие типы данных от GUI и от конкретных интеграторов.
// 3) Использовать единый формат результата по всему проекту.
// ============================================================================

// Защита от повторного включения заголовочного файла.
#pragma once

// Подключаем вектор состояния, который используется в каждой точке траектории.
#include "TVector.h"

// std::array нужен для компактного хранения фиксированного состояния КА.
#include <array>
// std::string нужен для текстового описания статуса расчета.
#include <string>
// std::vector нужен для хранения массива точек траектории.
#include <vector>

// ----------------------------------------------------------------------------
// ПСЕВДОНИМ SimulationState
// ----------------------------------------------------------------------------
// Назначение:
// Хранить состояние КА в компактном фиксированном виде:
// (x, y, z, Vx, Vy, Vz).
//
// В отличие от TVector, std::array<double, 6> не делает отдельного
// динамического выделения памяти на каждую точку траектории.
// Это заметно снижает расход памяти при очень больших расчетах.
// ----------------------------------------------------------------------------
using SimulationState = std::array<double, 6>;

// ----------------------------------------------------------------------------
// СТРУКТУРА SimulationSample
// ----------------------------------------------------------------------------
// Назначение:
// Хранить одно состояние системы в конкретный момент времени.
//
// Такая структура соответствует одной строке траектории:
// - время;
// - полный вектор состояния КА в это время.
// ----------------------------------------------------------------------------
struct SimulationSample
{
    // Момент времени, которому соответствует данное состояние.
    double time = 0.0;

    // Полный вектор состояния КА:
    // (x, y, z, Vx, Vy, Vz).
    SimulationState state{};
};

// ----------------------------------------------------------------------------
// СТРУКТУРА SimulationResult
// ----------------------------------------------------------------------------
// Назначение:
// Хранить итог численного моделирования целиком.
//
// Содержит:
// - флаг успеха или неуспеха;
// - текстовое сообщение;
// - накопленный массив точек траектории.
//
// Такой формат удобен тем, что даже при досрочной остановке расчета
// пользователь все равно может получить уже рассчитанную часть траектории.
// ----------------------------------------------------------------------------
struct SimulationResult
{
    // Признак успешного завершения расчета.
    bool success = false;

    // Сообщение о результате расчета:
    // - успешное завершение;
    // - ошибка;
    // - предупреждение;
    // - досрочная остановка.
    std::string message;

    // Все рассчитанные точки траектории в порядке возрастания времени.
    std::vector<SimulationSample> samples;
};

// ----------------------------------------------------------------------------
// ФУНКЦИЯ MakeSimulationSample
// ----------------------------------------------------------------------------
// Назначение:
// Преобразовать внутренний TVector интегратора в компактную структуру
// SimulationSample для долговременного хранения результата.
// ----------------------------------------------------------------------------
inline SimulationSample MakeSimulationSample(double time, const TVector& state)
{
    if (state.Size() != 6)
    {
        throw std::invalid_argument("Состояние КА должно содержать ровно 6 компонентов");
    }

    return {time, {state[0], state[1], state[2], state[3], state[4], state[5]}};
}
