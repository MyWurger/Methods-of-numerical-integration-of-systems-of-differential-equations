// ============================================================================
// ФАЙЛ TABSTRACTINTEGRATOR.H - АБСТРАКТНЫЙ БАЗОВЫЙ КЛАСС ИНТЕГРАТОРА
// ============================================================================
// Назначение файла:
// 1) Объявить общий интерфейс для всех численных методов интегрирования.
// 2) Вынести в один базовый класс общую механику работы с состоянием,
//    временем и правыми частями системы ОДУ.
// 3) Оставить производным классам только реализацию одного шага метода.
// ============================================================================

// Защита от повторного включения заголовочного файла.
#pragma once

// Подключаем общие типы результата моделирования.
#include "SimulationTypes.h"
// Подключаем интерфейс модели правых частей системы ОДУ.
#include "TDynamicModel.h"

// ----------------------------------------------------------------------------
// КЛАСС TAbstractIntegrator - БАЗОВЫЙ КЛАСС ДЛЯ МЕТОДОВ ИНТЕГРИРОВАНИЯ
// ----------------------------------------------------------------------------
// Класс отвечает за:
// - хранение параметров интегрирования t0, tk и h;
// - хранение текущего состояния системы;
// - хранение ссылки на модель правых частей;
// - общий цикл интегрирования до момента tend;
// - накопление массива точек результата.
//
// Производные классы должны определить только функцию OneStep(...),
// которая реализует один шаг конкретного численного метода.
// ----------------------------------------------------------------------------
class TAbstractIntegrator
{
public:
    // Конструктор базового интегратора.
    //
    // Принимает:
    // - t0 : начальный момент времени;
    // - tk : конечный момент времени;
    // - h  : шаг интегрирования.
    TAbstractIntegrator(double t0, double tk, double h);

    // Виртуальный деструктор нужен для корректного удаления производных
    // объектов через указатель на базовый класс.
    virtual ~TAbstractIntegrator() = default;

    // Устанавливает модель правых частей системы ОДУ.
    void SetRightParts(const TDynamicModel& model);

    // Устанавливает начальное состояние системы и переводит текущее время
    // обратно в t0.
    void SetInitialState(const TVector& state);

    // Возвращает начальный момент времени интегрирования.
    double GetT0() const noexcept;

    // Возвращает конечный момент времени интегрирования.
    double GetTk() const noexcept;

    // Возвращает шаг интегрирования.
    double GetH() const noexcept;

    // Выполняет численное интегрирование до момента tend.
    //
    // Возвращает:
    // - флаг успеха;
    // - сообщение о результате;
    // - массив накопленных точек траектории.
    SimulationResult MoveTo(double tend);

protected:
    // Выполняет один шаг конкретного численного метода.
    //
    // Этот метод обязан реализовать производный класс.
    virtual TVector OneStep(const TVector& state) = 0;

    // Возвращает установленную модель правых частей.
    const TDynamicModel& Model() const;

    // Проверяет, что все компоненты состояния являются конечными числами.
    static void EnsureFiniteState(const TVector& state);

    // Возвращает текущее время внутри цикла интегрирования.
    double GetCurrentTime() const noexcept;

    // Возвращает текущее состояние системы.
    const TVector& GetCurrentState() const noexcept;

    // Возвращает текущий шаг интегрирования.
    // Он может временно уменьшаться на последнем шаге, чтобы точно дойти до
    // конечного времени.
    double GetCurrentH() const noexcept;

    // Сдвигает внутреннее текущее время на величину step.
    void AdvanceTime(double step) noexcept;

private:
    // Начальный момент времени интегрирования.
    double t0_;

    // Конечный момент времени интегрирования.
    double tk_;

    // Базовый шаг интегрирования.
    double h_;

    // Текущее время внутри процесса интегрирования.
    double currentTime_;

    // Текущее состояние системы.
    TVector currentState_;

    // Указатель на модель правых частей системы.
    // Сам объект модели интегратору не принадлежит.
    const TDynamicModel* rightParts_ = nullptr;
};
