// Назначение файла:
// 1) Объявить абстрактный интерфейс динамической модели.
// 2) Задать единый контракт для вычисления правых частей системы ОДУ.
// 3) Дать возможность разным моделям движения подключаться к одному и тому
//    же интегратору через полиморфизм.
// ============================================================================

// Защита от повторного включения заголовочного файла.
#pragma once

// Подключаем тип TVector, используемый для состояния системы и правых частей.
#include "TVector.h"

// std::string нужен для сообщений о терминальном состоянии системы.
#include <string>

// ----------------------------------------------------------------------------
// КЛАСС TDynamicModel - АБСТРАКТНАЯ МОДЕЛЬ ПРАВЫХ ЧАСТЕЙ СИСТЕМЫ ОДУ
// ----------------------------------------------------------------------------
// Класс отвечает за:
// - вычисление вектора правых частей F(t, X);
// - проверку терминального состояния системы, если оно поддерживается
//   конкретной моделью.
//
// Производные классы должны реализовать конкретную физическую модель,
// например движение КА в центральном поле Земли.
// ----------------------------------------------------------------------------
class TDynamicModel
{
public:
    // Виртуальный деструктор нужен для корректной работы с производными
    // объектами через указатель или ссылку на базовый класс.
    virtual ~TDynamicModel() = default;

    // Вычисляет правые части системы ОДУ в момент времени time для состояния
    // state.
    //
    // Возвращает вектор:
    //     F(t, X)
    // который затем используется численным интегратором.
    virtual TVector Funcs(double time, const TVector& state) const = 0;

    // Проверяет, достигла ли система терминального состояния.
    //
    // По умолчанию базовая модель может не иметь никакого специального
    // условия остановки, поэтому конкретный смысл этого метода задают
    // производные классы.
    //
    // Возвращает:
    // - true  : если расчет нужно остановить;
    // - false : если моделирование можно продолжать.
    //
    // В message записывается пояснение причины остановки.
    virtual bool IsTerminalState(const TVector& state, std::string& message) const
    {
        // Базовая реализация не задает никакого специального условия
        // остановки.
        //
        // Это означает:
        // - state здесь не анализируется;
        // - message не заполняется;
        // - по умолчанию моделирование можно продолжать.
        //
        // Конкретные производные модели, например TSpaceCraft, могут
        // переопределить этот метод и задать свое терминальное условие.
        (void)state;
        (void)message;
        return false;
    }
};
