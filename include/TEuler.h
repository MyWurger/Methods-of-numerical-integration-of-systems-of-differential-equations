// ============================================================================
// ФАЙЛ TEULER.H - ОБЪЯВЛЕНИЕ МЕТОДА ЭЙЛЕРА
// ============================================================================
// Назначение файла:
// 1) Объявить класс TEuler как конкретную реализацию базового интегратора.
// 2) Реализовать один шаг явного метода Эйлера.
// 3) Использовать общую механику интегрирования из TAbstractIntegrator.
// ============================================================================

// Защита от повторного включения заголовочного файла.
#pragma once

// Подключаем базовый класс интегратора, от которого наследуется метод Эйлера.
#include "TAbstractIntegrator.h"

// ----------------------------------------------------------------------------
// КЛАСС TEuler - КОНКРЕТНАЯ РЕАЛИЗАЦИЯ МЕТОДА ЭЙЛЕРА
// ----------------------------------------------------------------------------
// Класс отвечает за:
// - реализацию одного шага метода Эйлера;
// - использование общего цикла интегрирования базового класса.
//
// Формула одного шага имеет вид:
//     X_(n+1) = X_n + h * F(t_n, X_n)
// ----------------------------------------------------------------------------
class TEuler final : public TAbstractIntegrator
{
public:
    // Конструктор метода Эйлера.
    //
    // Принимает:
    // - t0 : начальный момент времени;
    // - tk : конечный момент времени;
    // - h  : шаг интегрирования.
    TEuler(double t0, double tk, double h);

protected:
    // Реализует один шаг явного метода Эйлера для текущего состояния.
    //
    // override означает, что этот метод обязан переопределять виртуальный
    // метод базового класса TAbstractIntegrator.
    //
    // Если сигнатура здесь случайно не совпадет с базовым методом, компилятор
    // выдаст ошибку. Это защита от опечаток и неверного переопределения.
    TVector OneStep(const TVector& state) override;
};
