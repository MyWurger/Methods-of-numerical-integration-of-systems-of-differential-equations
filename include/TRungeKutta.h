// Назначение файла:
// 1) Объявить класс TRungeKutta как конкретную реализацию базового
//    интегратора.
// 2) Реализовать один шаг классического метода Рунге-Кутты 4 порядка.
// 3) Использовать общую механику интегрирования из TAbstractIntegrator.
// ============================================================================

// Защита от повторного включения заголовочного файла.
#pragma once

// Подключаем базовый класс интегратора, от которого наследуется метод
// Рунге-Кутты.
#include "TAbstractIntegrator.h"

// ----------------------------------------------------------------------------
// КЛАСС TRungeKutta - КОНКРЕТНАЯ РЕАЛИЗАЦИЯ МЕТОДА РУНГЕ-КУТТЫ 4 ПОРЯДКА
// ----------------------------------------------------------------------------
// Класс отвечает за:
// - реализацию одного шага метода Рунге-Кутты 4 порядка;
// - использование общего цикла интегрирования базового класса.
//
// Формула одного шага имеет вид:
//     X_(n+1) = X_n + h/6 * (k1 + 2k2 + 2k3 + k4)
// ----------------------------------------------------------------------------
class TRungeKutta final : public TAbstractIntegrator
{
public:
    // Конструктор метода Рунге-Кутты 4 порядка.
    //
    // Принимает:
    // - t0 : начальный момент времени;
    // - tk : конечный момент времени;
    // - h  : шаг интегрирования.
    TRungeKutta(double t0, double tk, double h);

protected:
    // Реализует один шаг классического метода Рунге-Кутты 4 порядка.
    //
    // override означает, что этот метод переопределяет виртуальный метод
    // базового класса TAbstractIntegrator.
    //
    // Такое ключевое слово помогает компилятору проверить, что мы действительно
    // корректно переопределяем OneStep(...), а не объявляем новый метод по
    // ошибке.
    TVector OneStep(const TVector& state) override;
};
