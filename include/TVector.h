// ============================================================================
// ФАЙЛ TVECTOR.H - ПРОСТОЙ ВЕКТОР ВЕЩЕСТВЕННЫХ ЧИСЕЛ
// ============================================================================
// Назначение файла:
// 1) Объявить класс TVector как базовый контейнер для хранения состояния
//    системы и векторов правых частей.
// 2) Реализовать основные арифметические операции над векторами.
// 3) Дать проекту единый численный тип для координат, скоростей и промежуточных
//    вычислений интеграторов.
// ============================================================================

// Защита от повторного включения заголовочного файла.
#pragma once

// std::size_t нужен для размерностей и индексов элементов вектора.
#include <cstddef>
// std::initializer_list нужен для удобного создания вектора из списка значений.
#include <initializer_list>
// std::invalid_argument используется для сообщений об ошибках размерности и деления на ноль.
#include <stdexcept>
// std::vector является внутренним контейнером для хранения чисел.
#include <vector>

// ----------------------------------------------------------------------------
// КЛАСС TVector - ПРОСТОЙ ВЕКТОР ВЕЩЕСТВЕННЫХ ЧИСЕЛ
// ----------------------------------------------------------------------------
// Класс отвечает за:
// - хранение массива чисел double;
// - доступ к элементам по индексу;
// - передачу данных в арифметические операции.
//
// В проекте этот класс используется для:
// - вектора состояния КА;
// - вектора правых частей системы ОДУ;
// - промежуточных вычислений методов Эйлера и Рунге-Кутты.
// ----------------------------------------------------------------------------
class TVector
{
public:
    // Конструктор пустого вектора.
    TVector() = default;

    // Конструктор вектора заданной размерности.
    //
    // explicit запрещает неявное создание TVector из одного числа size.
    //
    // Иначе компилятор мог бы пытаться неявно преобразовать размерность в
    // объект вектора, что здесь нежелательно.
    //
    // Принимает:
    // - size  : число элементов;
    // - value : начальное значение каждого элемента.
    explicit TVector(std::size_t size, double value = 0.0) : data_(size, value)
    {
        // Здесь используется конструктор std::vector(size, value).
        //
        // Это означает:
        // 1) создать внутри data_ ровно size элементов;
        // 2) каждый элемент сразу заполнить значением value.
        //
        // Например:
        //     TVector v(3, 1.5)
        // создаст вектор из трех чисел:
        //     [1.5, 1.5, 1.5]
    }

    // Конструктор вектора из списка значений.
    //
    // Позволяет удобно писать:
    //     TVector v{1.0, 2.0, 3.0};
    TVector(std::initializer_list<double> values) : data_(values)
    {
        // std::initializer_list<double> — это специальный тип C++, который
        // позволяет передавать набор значений в фигурных скобках.
        //
        // То есть запись:
        //     TVector v{1.0, 2.0, 3.0};
        // приходит сюда как список values, после чего внутренний std::vector
        // копирует эти числа в data_.
    }

    // Возвращает размерность вектора.
    std::size_t Size() const noexcept
    {
        // data_.size() — стандартный способ получить число элементов во
        // внутреннем std::vector.
        return data_.size();
    }

    // Возвращает true, если вектор пуст.
    bool Empty() const noexcept
    {
        // empty() возвращает true, если внутри нет ни одного элемента.
        return data_.empty();
    }

    // Неконстантный доступ к элементу по индексу.
    //
    // Используется .at(...), поэтому при выходе за границы будет выброшено
    // исключение стандартной библиотеки.
    double& operator[](std::size_t index)
    {
        // Мы намеренно используем не operator[] у std::vector, а метод at().
        //
        // Разница такая:
        // - обычный vector[index] не проверяет границы;
        // - vector.at(index) проверяет границы и бросает исключение, если
        //   индекс неверен.
        //
        // Для учебного проекта это полезнее и безопаснее.
        return data_.at(index);
    }

    // Константный доступ к элементу по индексу.
    const double& operator[](std::size_t index) const
    {
        // Константная версия нужна для тех случаев, когда сам объект TVector
        // менять нельзя, но читать его элементы можно.
        return data_.at(index);
    }

    // Возвращает константную ссылку на внутренний std::vector<double>.
    const std::vector<double>& Data() const noexcept
    {
        // Возвращаем ссылку, а не копию, чтобы не создавать лишний новый
        // массив чисел.
        return data_;
    }

    // Возвращает изменяемую ссылку на внутренний std::vector<double>.
    std::vector<double>& Data() noexcept
    {
        // Эта версия позволяет вызывающему коду не только читать, но и
        // менять содержимое внутреннего контейнера.
        return data_;
    }

private:
    // Внутренний контейнер хранения координат вектора.
    std::vector<double> data_;
};

// ----------------------------------------------------------------------------
// ФУНКЦИЯ EnsureSameSize
// ----------------------------------------------------------------------------
// Назначение:
// Проверить, что два вектора имеют одинаковую размерность.
//
// Если размерности различаются, выбрасывается исключение.
// ----------------------------------------------------------------------------
inline void EnsureSameSize(const TVector& lhs, const TVector& rhs)
{
    // Сначала сравниваем размерности левого и правого операнда.
    if (lhs.Size() != rhs.Size())
    {
        // throw std::invalid_argument(...) означает:
        // выполнение текущей функции немедленно прерывается, и наружу
        // передается исключение с поясняющим текстом.
        //
        // Здесь это нужно, чтобы запретить операции над векторами разной
        // длины, например:
        //     (1, 2, 3) + (4, 5)
        throw std::invalid_argument("Векторы должны иметь одинаковую размерность");
    }
}

// ----------------------------------------------------------------------------
// ОПЕРАТОР operator+
// ----------------------------------------------------------------------------
// Назначение:
// Выполнить покомпонентное сложение двух векторов одинаковой размерности.
// ----------------------------------------------------------------------------
inline TVector operator+(const TVector& lhs, const TVector& rhs)
{
    // Сложение имеет смысл только для векторов одинаковой размерности.
    EnsureSameSize(lhs, rhs);

    // Создаем результирующий вектор той же размерности, что и lhs/rhs.
    TVector result(lhs.Size());

    // Идем по всем координатам и складываем их покомпонентно.
    for (std::size_t i = 0; i < lhs.Size(); ++i)
    {
        result[i] = lhs[i] + rhs[i];
    }

    // Возвращаем уже готовый новый вектор-сумму.
    return result;
}

// ----------------------------------------------------------------------------
// ОПЕРАТОР operator-
// ----------------------------------------------------------------------------
// Назначение:
// Выполнить покомпонентное вычитание двух векторов одинаковой размерности.
// ----------------------------------------------------------------------------
inline TVector operator-(const TVector& lhs, const TVector& rhs)
{
    // Вычитание тоже допускается только для одинаковых размерностей.
    EnsureSameSize(lhs, rhs);

    // Создаем новый вектор-результат.
    TVector result(lhs.Size());

    // Для каждой координаты вычитаем значение rhs из lhs.
    for (std::size_t i = 0; i < lhs.Size(); ++i)
    {
        result[i] = lhs[i] - rhs[i];
    }

    // Возвращаем результирующий вектор.
    return result;
}

// ----------------------------------------------------------------------------
// ОПЕРАТОР operator*(vector, scalar)
// ----------------------------------------------------------------------------
// Назначение:
// Умножить вектор на скаляр справа.
// ----------------------------------------------------------------------------
inline TVector operator*(const TVector& vector, double scalar)
{
    // Результат имеет ту же размерность, что и исходный вектор.
    TVector result(vector.Size());

    // Каждая координата умножается на одно и то же число scalar.
    for (std::size_t i = 0; i < vector.Size(); ++i)
    {
        result[i] = vector[i] * scalar;
    }

    // Возвращаем новый масштабированный вектор.
    return result;
}

// ----------------------------------------------------------------------------
// ОПЕРАТОР operator*(scalar, vector)
// ----------------------------------------------------------------------------
// Назначение:
// Умножить вектор на скаляр слева.
// ----------------------------------------------------------------------------
inline TVector operator*(double scalar, const TVector& vector)
{
    // Умножение числа на вектор слева по смыслу эквивалентно умножению
    // вектора на число справа, поэтому мы просто переиспользуем уже
    // реализованный оператор выше.
    return vector * scalar;
}

// ----------------------------------------------------------------------------
// ОПЕРАТОР operator/(vector, scalar)
// ----------------------------------------------------------------------------
// Назначение:
// Разделить все компоненты вектора на скаляр.
//
// Деление на ноль запрещено и приводит к исключению.
// ----------------------------------------------------------------------------
inline TVector operator/(const TVector& vector, double scalar)
{
    // Сначала проверяем наиболее опасный случай — деление на ноль.
    if (scalar == 0.0)
    {
        // Деление на ноль математически недопустимо, поэтому здесь сразу
        // выбрасывается исключение.
        throw std::invalid_argument("Деление вектора на ноль недопустимо");
    }

    // Результирующий вектор имеет ту же размерность, что и исходный.
    TVector result(vector.Size());

    // Каждая координата делится на scalar отдельно.
    for (std::size_t i = 0; i < vector.Size(); ++i)
    {
        result[i] = vector[i] / scalar;
    }

    // Возвращаем новый вектор-результат деления.
    return result;
}
