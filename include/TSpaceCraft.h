// ============================================================================
// Назначение файла:
// 1) Объявить класс TSpaceCraft как конкретную реализацию динамической модели.
// 2) Описать вычисление правых частей системы ОДУ для движения КА в
//    центральном гравитационном поле Земли.
// 3) Описать условие терминального состояния при столкновении с Землей.
// ============================================================================

// Защита от повторного включения заголовочного файла.
#pragma once

// Подключаем базовый интерфейс динамической модели.
#include "TDynamicModel.h"

// ----------------------------------------------------------------------------
// КЛАСС TSpaceCraft - МОДЕЛЬ КОСМИЧЕСКОГО АППАРАТА В ЦЕНТРАЛЬНОМ ПОЛЕ ЗЕМЛИ
// ----------------------------------------------------------------------------
// Класс отвечает за:
// - вычисление правых частей системы движения КА;
// - использование гравитационного параметра Земли mu;
// - контроль терминального состояния при достижении радиуса Земли.
//
// Вектор состояния имеет вид:
//     X = (x, y, z, Vx, Vy, Vz)
//
// Правые части системы имеют вид:
//     x'  = Vx
//     y'  = Vy
//     z'  = Vz
//     Vx' = -mu * x / r^3
//     Vy' = -mu * y / r^3
//     Vz' = -mu * z / r^3
// ----------------------------------------------------------------------------
class TSpaceCraft final : public TDynamicModel
{
public:
    // Конструктор модели космического аппарата.
    //
    // explicit запрещает неявное создание объекта TSpaceCraft из одного
    // числа mu.
    //
    // Иначе выражение вроде:
    //     TSpaceCraft model = 3.98603e14;
    // было бы допустимо как неявное преобразование.
    //
    // Здесь мы требуем явного создания объекта модели.
    explicit TSpaceCraft(double mu = 3.98603e14);

    // Вычисляет правые части системы ОДУ для текущего состояния КА.
    //
    // override означает, что этот метод переопределяет виртуальный метод
    // базового интерфейса TDynamicModel.
    //
    // Компилятор при этом проверяет, что сигнатура точно совпадает с
    // объявлением в базовом классе.
    TVector Funcs(double time, const TVector& state) const override;

    // Проверяет, достиг ли КА терминального состояния.
    //
    // В данной модели терминальное состояние означает столкновение с Землей,
    // то есть ситуацию, когда расстояние до центра Земли стало не больше
    // радиуса Земли.
    //
    // Здесь override имеет тот же смысл:
    // метод не новый, а именно переопределенный из TDynamicModel.
    bool IsTerminalState(const TVector& state, std::string& message) const override;

private:
    // Гравитационный параметр Земли.
    double mu_;

    // Радиус Земли, используемый для определения столкновения.
    const double earthRadius_ = 6.371e6;
};
