// Подключаем объявление класса TEuler.
#include "TEuler.h"

// ----------------------------------------------------------------------------
// КОНСТРУКТОР TEuler
// ----------------------------------------------------------------------------
// Назначение:
// Создать объект интегратора, работающего по методу Эйлера.
//
// Принимает:
// - t0 - начальный момент времени;
// - tk - конечный момент времени;
// - h  - шаг интегрирования.
//
// Что делает:
// Сам конструктор TEuler не добавляет собственной логики, а просто передает
// все параметры в конструктор базового класса TAbstractIntegrator.
// ----------------------------------------------------------------------------
TEuler::TEuler(double t0, double tk, double h) : TAbstractIntegrator(t0, tk, h)
{
}

// ----------------------------------------------------------------------------
// МЕТОД OneStep
// ----------------------------------------------------------------------------
// Назначение:
// Выполнить один шаг метода Эйлера для текущего состояния системы.
//
// Теоретическая формула метода Эйлера:
// X(i+1) = X(i) + h * F(t(i), X(i))
//
// Как эта формула читается:
// 1) берем текущее состояние X(i);
// 2) вычисляем правые части системы F(t(i), X(i));
// 3) умножаем их на шаг h;
// 4) прибавляем полученное приращение к текущему состоянию.
//
// В нашей задаче:
// - state            играет роль X(i);
// - GetCurrentH()    возвращает текущий шаг h;
// - GetCurrentTime() возвращает текущее время t(i);
// - Model().Funcs(...) вычисляет вектор правых частей F(t(i), X(i)).
// ----------------------------------------------------------------------------
TVector TEuler::OneStep(const TVector& state)
{
    // Сначала вычисляем правые части системы ОДУ в текущей точке:
    // F(t(i), X(i)).
    //
    // Для задачи движения КА это вектор вида:
    // [x', y', z', Vx', Vy', Vz'].
    //
    // Здесь:
    // - производные координат равны соответствующим компонентам скорости;
    // - производные скоростей равны компонентам гравитационного ускорения.
    const TVector derivatives = Model().Funcs(GetCurrentTime(), state);

    // Затем реализуем сам шаг Эйлера:
    // новое состояние = старое состояние + шаг * производная.
    return state + GetCurrentH() * derivatives;
}
