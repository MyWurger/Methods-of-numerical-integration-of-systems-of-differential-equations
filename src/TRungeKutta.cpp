// Назначение файла:
// 1) реализовать конструктор класса TRungeKutta;
// 2) реализовать формулу одного шага классического метода Рунге-Кутты
//    четвертого порядка;
// 3) конкретизировать абстрактный интерфейс TAbstractIntegrator для более
//    точного метода по сравнению с Эйлером.
//
// Общая механика движения по времени, как и в случае TEuler, уже находится
// в базовом классе TAbstractIntegrator. Поэтому здесь остается только
// правильно реализовать математическую формулу одного шага RK4.
// ============================================================================

// Подключаем объявление класса TRungeKutta.
#include "TRungeKutta.h"

// ----------------------------------------------------------------------------
// КОНСТРУКТОР TRungeKutta
// ----------------------------------------------------------------------------
// Назначение:
// Создать объект интегратора, который будет выполнять шаги по методу
// Рунге-Кутты 4 порядка.
//
// Принимает:
// - t0 - начальный момент времени;
// - tk - конечный момент времени;
// - h  - шаг интегрирования.
//
// Как и у TEuler, собственная логика здесь не добавляется:
// параметры просто передаются базовому классу.
// ----------------------------------------------------------------------------
TRungeKutta::TRungeKutta(double t0, double tk, double h) : TAbstractIntegrator(t0, tk, h)
{
}

// ----------------------------------------------------------------------------
// МЕТОД OneStep
// ----------------------------------------------------------------------------
// Назначение:
// Выполнить один шаг интегрирования по классической формуле RK4.
//
// Теоретическая формула:
// k1 = F(t(i),         X(i))
// k2 = F(t(i) + h/2,   X(i) + h/2 * k1)
// k3 = F(t(i) + h/2,   X(i) + h/2 * k2)
// k4 = F(t(i) + h,     X(i) + h   * k3)
//
// X(i+1) = X(i) + h/6 * (k1 + 2*k2 + 2*k3 + k4)
//
// Смысл метода:
// Вместо того чтобы брать наклон только в одной точке, как делает метод
// Эйлера, RK4 оценивает правые части сразу в нескольких точках внутри шага
// и затем берет их взвешенное среднее.
//
// Именно поэтому RK4 обычно дает существенно более точный результат.
// ----------------------------------------------------------------------------
TVector TRungeKutta::OneStep(const TVector& state)
{
    // Сохраняем текущее время и текущий шаг в локальные переменные.
    // Это делает запись ниже короче и удобнее для чтения.
    const double time = GetCurrentTime();
    const double step = GetCurrentH();

    // k1 - оценка правых частей в начале шага.
    // Это аналог наклона в точке (t(i), X(i)).
    const TVector k1 = Model().Funcs(time, state);

    // k2 - оценка правых частей в середине шага, но уже с учетом k1.
    // То есть мы делаем предварительный "пробный полушаг" вперед.
    const TVector k2 = Model().Funcs(time + step * 0.5, state + (step * 0.5) * k1);

    // k3 - еще одна оценка в середине шага, но теперь уже на основе k2.
    // Это уточняет информацию о поведении системы внутри интервала.
    const TVector k3 = Model().Funcs(time + step * 0.5, state + (step * 0.5) * k2);

    // k4 - оценка правых частей в конце шага.
    // Здесь используется полный шаг по времени и прогноз на его конец.
    const TVector k4 = Model().Funcs(time + step, state + step * k3);

    // Собираем итоговое новое состояние по формуле RK4.
    //
    // Почему веса именно такие:
    // - k1 берется один раз;
    // - k2 и k3 считаются более информативными для середины интервала,
    //   поэтому имеют удвоенный вес;
    // - k4 тоже берется один раз.
    //
    // Деление на 6.0 завершает формулу взвешенного усреднения.
    return state + (step / 6.0) * (k1 + 2.0 * k2 + 2.0 * k3 + k4);
}
