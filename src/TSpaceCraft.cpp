// Назначение файла:
// 1) реализовать конкретную динамическую модель TSpaceCraft;
// 2) вычислять правые части системы ОДУ для движения КА в центральном
//    гравитационном поле Земли;
// 3) определять терминальное состояние "столкновение с Землей".
//
// Именно этот файл отвечает за физический смысл модели:
// - интеграторы TEuler и TRungeKutta знают только "как интегрировать";
// - TSpaceCraft знает "что именно интегрировать".
//
// Иначе говоря:
// - методы интегрирования описывают численный алгоритм;
// - TSpaceCraft описывает сами уравнения движения.
// ============================================================================

// Подключаем объявление класса TSpaceCraft.
#include "TSpaceCraft.h"

// <cmath> нужен для sqrt(...), то есть вычисления длины радиус-вектора.
#include <cmath>

// <stdexcept> нужен для генерации исключений при некорректных входных данных.
#include <stdexcept>

// ----------------------------------------------------------------------------
// КОНСТРУКТОР TSpaceCraft
// ----------------------------------------------------------------------------
// Назначение:
// Создать модель движения КА с заданным гравитационным параметром.
//
// Принимает:
// mu соответствует гравитационному параметру Земли.
// ----------------------------------------------------------------------------
TSpaceCraft::TSpaceCraft(double mu) : mu_(mu)
{
}

// ----------------------------------------------------------------------------
// МЕТОД Funcs
// ----------------------------------------------------------------------------
// Назначение:
// Вычислить правые части системы ОДУ для текущего состояния КА.
//
// Вектор состояния в данной работе имеет вид:
// X = [x, y, z, Vx, Vy, Vz]
//
// Тогда вектор правых частей имеет вид:
// F(t, X) = [x', y', z', Vx', Vy', Vz']
//
// Согласно физической постановке задачи:
// - x'  = Vx
// - y'  = Vy
// - z'  = Vz
// - Vx' = ax
// - Vy' = ay
// - Vz' = az
//
// А в центральном гравитационном поле Земли:
// ax = -mu * x / r^3
// ay = -mu * y / r^3
// az = -mu * z / r^3
// где r = sqrt(x^2 + y^2 + z^2)
//
// Именно этот метод и возвращает вектор:
// [Vx, Vy, Vz, ax, ay, az]
// ----------------------------------------------------------------------------
TVector TSpaceCraft::Funcs(double /*time*/, const TVector& state) const
{
    // Сначала убеждаемся, что вектор состояния действительно имеет
    // ожидаемую размерность.
    //
    // В данной задаче у состояния должно быть ровно 6 компонент:
    // x, y, z, Vx, Vy, Vz.
    if (state.Size() != 6)
    {
        // throw в C++ означает "сгенерировать исключение", то есть аварийно
        // сообщить вызывающему коду о том, что дальше нормальное вычисление
        // продолжать нельзя.
        //
        // std::invalid_argument используется в тех случаях, когда в функцию
        // передали некорректный аргумент.
        //
        // Здесь именно такая ситуация: сама формула модели правильная,
        // но входной вектор состояния имеет неверную размерность.
        throw std::invalid_argument("Состояние КА должно содержать 6 компонент");
    }

    // Извлекаем координаты центра масс КА.
    const double x = state[0];
    const double y = state[1];
    const double z = state[2];

    // Извлекаем компоненты скорости КА.
    const double vx = state[3];
    const double vy = state[4];
    const double vz = state[5];

    // Вычисляем квадрат длины радиус-вектора:
    // r^2 = x^2 + y^2 + z^2
    //
    // Такой промежуточный шаг удобен, потому что он затем используется
    // и при проверке корректности, и при вычислении самого ускорения.
    const double radiusSquared = x * x + y * y + z * z;

    // Радиус-вектор не должен быть нулевым.
    // В точке r = 0 формула центрального поля становится некорректной,
    // потому что появляется деление на ноль.
    if (radiusSquared <= 0.0)
    {
        // Здесь снова используется throw, но уже с другим типом исключения:
        // std::runtime_error.
        //
        // В данном случае состояние приводит к физически и математически
        // недопустимой ситуации: радиус-вектор равен нулю, а значит формулу
        // ускорения в центральном поле применять нельзя.
        throw std::runtime_error("Радиус-вектор КА не должен быть нулевым");
    }

    // Теперь вычисляем сам радиус:
    // r = sqrt(r^2)
    const double radius = std::sqrt(radiusSquared);

    // Вычисляем общий множитель для всех трех компонент ускорения:
    // -mu / r^3
    //
    // Почему формула записана как:
    // -mu / (radiusSquared * radius)
    //
    // Потому что:
    // r^3 = r^2 * r
    //
    // А r^2 у нас уже посчитан в radiusSquared.
    const double factor = -mu_ / (radiusSquared * radius);

    // Возвращаем вектор правых частей системы.
    //
    // Первые три компоненты - это производные координат, то есть скорости.
    // Последние три компоненты - это производные скоростей, то есть
    // компоненты гравитационного ускорения.
    return {vx, vy, vz, factor * x, factor * y, factor * z};
}

// ----------------------------------------------------------------------------
// МЕТОД IsTerminalState
// ----------------------------------------------------------------------------
// Назначение:
// Проверить, не достигнуто ли терминальное состояние, при котором расчет
// нужно остановить.
//
// В данной работе таким состоянием считается столкновение с Землей.
// Мы трактуем его так:
// если расстояние от КА до центра Земли стало меньше или равно радиусу Земли,
// дальнейшее интегрирование прекращается.
// ----------------------------------------------------------------------------
bool TSpaceCraft::IsTerminalState(const TVector& state, std::string& message) const
{
    // Как и в Funcs(...), сначала проверяем корректность размерности.
    if (state.Size() != 6)
    {
        // И здесь throw используется как способ немедленно сообщить наружу,
        // что функция вызвана с неверным аргументом.
        //
        // Это лучше, чем молча продолжать работу с испорченными данными,
        // потому что ошибка обнаруживается сразу и явно.
        throw std::invalid_argument("Состояние КА должно содержать 6 компонент");
    }

    // Для проверки столкновения нужны только пространственные координаты.
    const double x = state[0];
    const double y = state[1];
    const double z = state[2];

    // Вычисляем расстояние от центра Земли до КА.
    const double radius = std::sqrt(x * x + y * y + z * z);

    // Если КА достиг поверхности Земли или оказался внутри нее,
    // считаем это терминальным состоянием.
    if (radius <= earthRadius_)
    {
        // Возвращаем наружу поясняющее сообщение, чтобы GUI мог показать
        // пользователю понятный статус остановки расчета.
        message = "Моделирование остановлено: столкновение с Землёй";
        return true;
    }

    // Если условие остановки не выполнено, интегрирование можно продолжать.
    return false;
}
