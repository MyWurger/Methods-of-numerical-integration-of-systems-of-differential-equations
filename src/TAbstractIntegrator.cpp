// Назначение файла:
// 1) реализовать общий каркас работы любого интегратора;
// 2) хранить параметры интегрирования и текущее состояние системы;
// 3) запускать цикл движения по времени;
// 4) делегировать вычисление одного шага конкретному методу-наследнику;
// 5) накапливать результат моделирования в виде набора выборок;
// 6) отслеживать терминальные состояния и ошибки входных данных.
//
// Это как раз делается в производных классах через виртуальный метод OneStep.
// ============================================================================

// Подключаем объявление базового класса интегратора.
#include "TAbstractIntegrator.h"

// <algorithm> нужен для std::min(...).
#include <algorithm>

// <cmath> нужен для std::ceil(...) и std::isfinite(...).
#include <cmath>

// <exception> нужен для перехвата стандартных исключений.
#include <exception>

// <limits> подключен как типичный системный заголовок для численных задач.
// Даже если прямо сейчас он почти не используется, он логически относится
// к низкоуровневой численной инфраструктуре этого файла.
#include <limits>

// ----------------------------------------------------------------------------
// АНОНИМНОЕ ПРОСТРАНСТВО ИМЕН
// ----------------------------------------------------------------------------
// namespace { ... } без имени создает сущности, видимые только внутри
// текущего .cpp файла.
//
// Это удобно для локальных констант, которые не должны быть доступны
// из других единиц трансляции.
// ----------------------------------------------------------------------------
namespace
{
// Допуск по времени.
// Из-за погрешностей double нельзя безусловно полагаться на точное равенство
// вида currentTime_ == effectiveEndTime, поэтому используется маленький
// числовой порог.
constexpr double kTimeTolerance = 1e-12;
}

// ----------------------------------------------------------------------------
// КОНСТРУКТОР TAbstractIntegrator
// ----------------------------------------------------------------------------
// Назначение:
// Инициализировать базовую часть интегратора.
//
// Принимает:
// - t0 - начальный момент времени;
// - tk - конечный момент времени;
// - h  - шаг интегрирования.
//
// Что происходит в списке инициализации:
// - сохраняем t0, tk, h;
// - текущее время currentTime_ сразу ставим равным t0.
// ----------------------------------------------------------------------------
TAbstractIntegrator::TAbstractIntegrator(double t0, double tk, double h)
    : t0_(t0), tk_(tk), h_(h), currentTime_(t0)
{
}

// ----------------------------------------------------------------------------
// МЕТОД SetRightParts
// ----------------------------------------------------------------------------
// Назначение:
// Передать интегратору объект динамической модели, который умеет вычислять
// правые части системы ОДУ.
//
// После этого интегратор сможет вызывать Model().Funcs(...).
// ----------------------------------------------------------------------------
void TAbstractIntegrator::SetRightParts(const TDynamicModel& model)
{
    // Сохраняем обычный указатель на уже существующий объект модели.
    rightParts_ = &model;
}

// ----------------------------------------------------------------------------
// МЕТОД SetInitialState
// ----------------------------------------------------------------------------
// Назначение:
// Задать начальное состояние интегрируемой системы.
//
// Что делаем:
// 1) проверяем, что состояние состоит из конечных чисел;
// 2) сохраняем его;
// 3) возвращаем текущее время к начальному t0_.
// ----------------------------------------------------------------------------
void TAbstractIntegrator::SetInitialState(const TVector& state)
{
    EnsureFiniteState(state);
    currentState_ = state;
    currentTime_ = t0_;
}

// Возвращает начальный момент времени интегрирования.
//
// noexcept означает, что функция по контракту не должна выбрасывать
// исключения. Это логично для простого геттера: он лишь возвращает уже
// сохраненное значение и не выполняет опасных операций.
double TAbstractIntegrator::GetT0() const noexcept
{
    return t0_;
}

// Возвращает конечный момент времени интегрирования.
// Здесь noexcept по той же причине: функция только читает поле объекта.
double TAbstractIntegrator::GetTk() const noexcept
{
    return tk_;
}

// Возвращает базовый шаг интегрирования.
// Для такого простого доступа к полю noexcept тоже естественен.
double TAbstractIntegrator::GetH() const noexcept
{
    return h_;
}

// ----------------------------------------------------------------------------
// МЕТОД MoveTo
// ----------------------------------------------------------------------------
// Назначение:
// Выполнить численное интегрирование от текущего момента времени до tend.
//
// Что делает метод:
// 1) проверяет корректность входных параметров;
// 2) формирует объект результата;
// 3) сохраняет начальную точку траектории;
// 4) в цикле многократно вызывает OneStep(...);
// 5) после каждого шага обновляет время и накапливает выборки;
// 6) останавливается при достижении tend, tk или терминального состояния;
// 7) в случае исключения возвращает SimulationResult с ошибкой.
// ----------------------------------------------------------------------------
SimulationResult TAbstractIntegrator::MoveTo(double tend)
{
    try
    {
        // Если правая часть системы не задана, интегрировать нечего.
        if (!rightParts_)
        {
            return {false, "Правая часть системы не задана", {}};
        }

        // Нельзя начинать интегрирование, если начальное состояние не было
        // установлено.
        if (currentState_.Empty())
        {
            return {false, "Начальное состояние не должно быть пустым", {}};
        }

        // Проверяем, что параметры интегрирования являются конечными числами.
        // std::isfinite(...) отсекает NaN и бесконечности.
        if (!std::isfinite(t0_) || !std::isfinite(tk_) || !std::isfinite(h_))
        {
            return {false, "Параметры интегрирования должны быть конечными числами", {}};
        }

        // Шаг интегрирования должен быть строго положительным.
        if (h_ <= 0.0)
        {
            return {false, "Шаг интегрирования h должен быть больше нуля", {}};
        }

        // Конечный момент времени не может быть меньше начального.
        if (tk_ < t0_)
        {
            return {false, "Конечный момент времени tk должен быть не меньше t0", {}};
        }

        // Пользовательский tend тоже должен быть конечным.
        if (!std::isfinite(tend))
        {
            return {false, "Момент времени tend должен быть конечным числом", {}};
        }

        // И он тоже не должен лежать левее t0_.
        if (tend < t0_)
        {
            return {false, "Момент времени tend должен быть не меньше t0", {}};
        }

        // Фактическое время окончания интегрирования ограничивается сверху tk_.
        // Иначе пользователь мог бы попросить идти дальше, чем допускает сам
        // объект интегратора.
        const double effectiveEndTime = std::min(tend, tk_);

        // Дополнительно убеждаемся, что текущее состояние состоит только из
        // корректных конечных чисел.
        EnsureFiniteState(currentState_);

        // Создаем объект результата и сразу задаем "успешное" состояние по
        // умолчанию. Если позже обнаружится терминальное событие или ошибка,
        // значения success/message будут скорректированы.
        SimulationResult result;
        result.success = true;
        result.message = "Моделирование завершено";

        // Оцениваем примерное число шагов, чтобы заранее выделить память
        // под массив выборок.
        //
        // span / h_ - примерно сколько шагов потребуется;
        // ceil(...) - округление вверх;
        // +1        - чтобы поместилась начальная точка.
        const double span = effectiveEndTime - currentTime_;
        const auto estimatedSteps =
            static_cast<std::size_t>(std::ceil(span / h_)) + static_cast<std::size_t>(1);

        // reserve(...) не меняет размер вектора, а только заранее просит
        // память, что уменьшает число переаллокаций при push_back(...).
        //
        // Одновременно сверху ставим ограничение 100000, чтобы не запрашивать
        // чрезмерный объем памяти уже на этапе оценки.
        result.samples.reserve(std::min<std::size_t>(estimatedSteps, 100000));

        // Сохраняем самую первую точку траектории: текущее время и текущее
        // состояние системы до начала цикла интегрирования.
        result.samples.push_back(MakeSimulationSample(currentTime_, currentState_));

        // В этой строке будет возвращено поясняющее сообщение, если модель
        // сообщит, что достигнуто терминальное состояние.
        std::string terminalMessage;

        // Проверяем терминальное состояние уже в самой начальной точке.
        // Например, КА может изначально находиться внутри Земли.
        if (Model().IsTerminalState(currentState_, terminalMessage))
        {
            result.success = false;
            result.message = terminalMessage;
            return result;
        }

        // Основной цикл интегрирования.
        // Идем вперед по времени, пока не приблизимся к effectiveEndTime
        // на расстояние меньше допуска kTimeTolerance.
        while ((effectiveEndTime - currentTime_) > kTimeTolerance)
        {
            // Обычно используется базовый шаг h_.
            // Но на последней итерации остаток времени может быть меньше h_.
            // Тогда нужно сделать укороченный последний шаг, чтобы точно
            // прийти в effectiveEndTime, а не перескочить его.
            const double integrationStep = std::min(h_, effectiveEndTime - currentTime_);

            // Сохраняем исходный шаг, потому что ниже временно подменим h_
            // на укороченный integrationStep.
            const double previousStep = h_;

            // Временно меняем шаг, чтобы конкретный метод OneStep(...)
            // использовал правильную длину текущего шага.
            h_ = integrationStep;

            // Вычисляем новое состояние через конкретный численный метод:
            // Эйлер, RK4 и т.д.
            currentState_ = OneStep(currentState_);

            // Возвращаем исходный базовый шаг обратно.
            h_ = previousStep;

            // Проверяем, что после шага не появились NaN или бесконечности.
            EnsureFiniteState(currentState_);

            // Продвигаем внутреннее время вперед на величину выполненного шага.
            AdvanceTime(integrationStep);

            // Сохраняем новую точку траектории.
            result.samples.push_back(MakeSimulationSample(currentTime_, currentState_));

            // После каждого шага снова проверяем, не достигнуто ли
            // терминальное состояние.
            if (Model().IsTerminalState(currentState_, terminalMessage))
            {
                result.success = false;
                result.message = terminalMessage;
                return result;
            }
        }

        // Если дошли сюда, значит интегрирование завершилось штатно.
        return result;
    }
    catch (const std::exception& ex)
    {
        // Если внутри вычислений возникло стандартное исключение, не даем
        // ему проброситься наружу в GUI напрямую.
        //
        // Вместо этого аккуратно превращаем его в SimulationResult с ошибкой,
        // чтобы приложение могло показать понятное сообщение пользователю.
        return {false, ex.what(), {}};
    }
}

// Возвращает ссылку на объект динамической модели.
const TDynamicModel& TAbstractIntegrator::Model() const
{
    return *rightParts_;
}

// Возвращает текущее внутреннее время интегратора.
// noexcept подчеркивает, что операция безопасна и не должна завершаться
// исключением.
double TAbstractIntegrator::GetCurrentTime() const noexcept
{
    return currentTime_;
}

// Возвращает текущее внутреннее состояние системы.
// Возврат ссылки на уже существующий объект также не требует генерации
// исключений, поэтому используется noexcept.
const TVector& TAbstractIntegrator::GetCurrentState() const noexcept
{
    return currentState_;
}

// Возвращает текущий шаг.
// Он может совпадать с базовым h_, а может временно отличаться на последнем
// укороченном шаге.
//
// noexcept здесь снова означает: функция только читает готовое поле.
double TAbstractIntegrator::GetCurrentH() const noexcept
{
    return h_;
}

// Продвигает внутреннее время интегратора вперед.
//
// noexcept означает, что операция рассматривается как безопасная:
// здесь происходит лишь простое сложение и присваивание значения полю.
void TAbstractIntegrator::AdvanceTime(double step) noexcept
{
    currentTime_ += step;
}

// ----------------------------------------------------------------------------
// МЕТОД EnsureFiniteState
// ----------------------------------------------------------------------------
// Назначение:
// Проверить, что все компоненты состояния являются корректными конечными
// числами.
//
// Если хотя бы одна компонента нечисловая (NaN) или бесконечная, дальнейшее
// интегрирование считается некорректным.
// ----------------------------------------------------------------------------
void TAbstractIntegrator::EnsureFiniteState(const TVector& state)
{
    // Проходим по всем значениям вектора состояния.
    for (double value : state.Data())
    {
        // std::isfinite(...) возвращает false для:
        // - NaN;
        // - +infinity;
        // - -infinity.
        if (!std::isfinite(value))
        {
            // throw используется для немедленного прерывания вычисления
            // и передачи информации об ошибке вверх по стеку вызовов.
            throw std::runtime_error("В состоянии системы обнаружено нечисловое значение");
        }
    }
}
