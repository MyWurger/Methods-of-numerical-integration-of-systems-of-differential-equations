// ----------------------------------------------------------------------------
// МЕТОД IsRecoverableResult
// ----------------------------------------------------------------------------
// Назначение:
// Определить, можно ли считать неуспешный результат все же полезным
// для показа пользователю.
//
// Принимает:
// - result - результат моделирования.
//
// Возвращает:
// - true, если расчет формально завершился неуспешно, но накопленные точки есть;
// - false в остальных случаях.
// ----------------------------------------------------------------------------
bool MainWindow::IsRecoverableResult(const SimulationResult& result)
{
    // Логика простая: результат recoverable, если success == false,
    // но samples не пуст.
    return !result.success && !result.samples.empty();
}
