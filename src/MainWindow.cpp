// КЛАСС MethodComboBox
// ----------------------------------------------------------------------------
// Назначение:
// Небольшая обертка над QComboBox, которая хранит флаг открытия popup-списка
// в dynamic property "popupOpen".
//
// Зачем это нужно:
// В stylesheet можно по-разному оформлять комбобокс в закрытом и открытом
// состоянии. Для этого и используется свойство popupOpen.
// ----------------------------------------------------------------------------
class MethodComboBox final : public QComboBox
{
public:
    // Конструктор принимает только стандартный parent-виджет.
    //
    // explicit запрещает неявное создание объекта из одного аргумента.
    explicit MethodComboBox(QWidget* parent = nullptr) : QComboBox(parent)
    {
        // setProperty(...) записывает dynamic property объекта Qt.
        // Здесь изначально считаем, что popup еще закрыт.
        setProperty("popupOpen", false);
    }

protected:
    // showPopup() вызывается Qt в момент открытия выпадающего списка.
    void showPopup() override
    {
        // Перед открытием popup помечаем состояние как "открыто".
        setProperty("popupOpen", true);

        // unpolish/polish заставляют Qt пересчитать стиль виджета после
        // изменения dynamic property.
        style()->unpolish(this);
        style()->polish(this);

        // update() запрашивает перерисовку виджета.
        update();

        // После нашей подготовки вызываем стандартное поведение базового класса.
        QComboBox::showPopup();
    }

    // hidePopup() вызывается Qt при закрытии списка.
    void hidePopup() override
    {
        // Сначала выполняем обычное закрытие popup средствами QComboBox.
        QComboBox::hidePopup();

        // Затем возвращаем свойство popupOpen в состояние "закрыто".
        setProperty("popupOpen", false);

        // И снова принудительно обновляем стиль и внешний вид виджета.
        style()->unpolish(this);
        style()->polish(this);
        update();
    }
};

class RichTextHeaderView final : public QHeaderView
{
public:
    // Конструктор принимает ориентацию заголовка и родительский виджет.
    //
    // Qt::Orientation определяет, будет ли заголовок горизонтальным
    // или вертикальным.
    explicit RichTextHeaderView(Qt::Orientation orientation, QWidget* parent = nullptr)
        : QHeaderView(orientation, parent)
    {
    }

protected:
    // paintSection(...) - виртуальный метод QHeaderView, отвечающий за
    // отрисовку одной секции заголовка таблицы.
    void paintSection(QPainter* painter, const QRect& rect, int logicalIndex) const override
    {
        // Из модели данных получаем текст заголовка для текущей секции.
        //
        // logicalIndex - логический номер столбца или строки заголовка.
        const QString text = model()->headerData(logicalIndex, orientation(), Qt::DisplayRole).toString();

        // Если в тексте нет HTML-подобной разметки, используем стандартную
        // отрисовку базового класса.
        if (!text.contains('<'))
        {
            QHeaderView::paintSection(painter, rect, logicalIndex);
            return;
        }

        // QStyleOptionHeader содержит набор параметров, нужных Qt-стилю
        // для стандартной отрисовки заголовка.
        QStyleOptionHeader option;

        // initStyleOption(...) заполняет option актуальными настройками
        // текущего виджета.
        initStyleOption(&option);
        option.rect = rect;
        option.section = logicalIndex;

        // Очищаем обычный текст, потому что сам текст будем рисовать
        // вручную через QTextDocument.
        option.text.clear();

        // drawControl(...) просит текущий стиль нарисовать саму "подложку"
        // секции заголовка без текста.
        style()->drawControl(QStyle::CE_Header, &option, painter, this);

        // QTextDocument используем как удобный инструмент для отрисовки
        // rich text / HTML-фрагмента.
        QTextDocument document;
        document.setDefaultFont(font());
        document.setDocumentMargin(0.0);

        // Через CSS задаем цвет, жирность и стиль нижних индексов.
        document.setDefaultStyleSheet(
            "body, div, span, sub { color: #203146; font-weight: 700; }"
            "sub { font-size: 70%; vertical-align: sub; }");

        // setHtml(...) загружает HTML-текст в документ.
        // QString::arg(text) подставляет строку заголовка в шаблон.
        document.setHtml(QString("<div style=\"text-align:center;\">%1</div>").arg(text));

        // Узнаем реальный размер, который занимает отрисованный HTML-текст.
        const QSizeF textSize = document.size();

        // Вычисляем левый верхний угол так, чтобы текст оказался
        // центрирован внутри прямоугольника секции.
        const QPointF topLeft(
            rect.x() + (rect.width() - textSize.width()) / 2.0,
            rect.y() + (rect.height() - textSize.height()) / 2.0);

        // save()/restore() сохраняют и восстанавливают состояние painter.
        // Это удобно, чтобы локально сдвинуть систему координат только
        // для данного фрагмента отрисовки.
        painter->save();
        painter->translate(topLeft);
        document.drawContents(painter);
        painter->restore();
    }
};

// ----------------------------------------------------------------------------
// ФУНКЦИЯ Radius
// ----------------------------------------------------------------------------
// Назначение:
// Вычислить модуль радиус-вектора КА по первым трем компонентам состояния.
//
// Принимает:
// - state - вектор состояния [x, y, z, Vx, Vy, Vz].
// ----------------------------------------------------------------------------
double Radius(const TVector& state)
{
    // Формула модуля радиус-вектора:
    // r = sqrt(x^2 + y^2 + z^2)
    return std::sqrt(state[0] * state[0] + state[1] * state[1] + state[2] * state[2]);
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ Speed
// ----------------------------------------------------------------------------
// Назначение:
// Вычислить модуль вектора скорости КА по последним трем компонентам
// состояния.
//
// Принимает:
// - state - вектор состояния [x, y, z, Vx, Vy, Vz].
// ----------------------------------------------------------------------------
double Speed(const TVector& state)
{
    // Формула модуля скорости:
    // |V| = sqrt(Vx^2 + Vy^2 + Vz^2)
    return std::sqrt(state[3] * state[3] + state[4] * state[4] + state[5] * state[5]);
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ FormatNumericValue
// ----------------------------------------------------------------------------
// Назначение:
// Преобразовать вещественное число в аккуратную строку для интерфейса.
//
// Что делает функция:
// - обрабатывает нечисловые значения;
// - убирает лишние нули в конце;
// - для слишком маленьких и слишком больших значений переходит
//   в экспоненциальный формат.
// ----------------------------------------------------------------------------
QString FormatNumericValue(double value)
{
    // Если значение не является конечным числом, явно сообщаем об этом.
    if (!std::isfinite(value))
    {
        return "не число";
    }

    // Ноль сразу возвращаем в коротком простом виде.
    if (value == 0.0)
    {
        return "0";
    }

    // std::abs(...) возвращает модуль числа.
    const double absValue = std::abs(value);

    // Для очень маленьких и очень больших чисел удобнее научная запись.
    if (absValue < 1e-4 || absValue >= 1e8)
    {
        // 'e' означает экспоненциальный формат, 5 - число знаков после запятой.
        return QString::number(value, 'e', 5);
    }

    // Для обычных значений сначала формируем десятичную запись с запасом.
    QString text = QString::number(value, 'f', 6);

    // chop(1) удаляет один символ с конца строки.
    // В цикле убираем лишние нули после запятой и, если нужно, саму точку.
    while (text.contains('.') && (text.endsWith('0') || text.endsWith('.')))
    {
        text.chop(1);
    }

    // Из-за особенностей округления строка иногда может превратиться в "-0".
    // Для интерфейса это лучше привести к обычному "0".
    if (text == "-0")
    {
        return "0";
    }

    // Возвращаем итоговую компактную строку.
    return text;
}

// ----------------------------------------------------------------------------
// КЛАСС SimulationTableModel
// ----------------------------------------------------------------------------
// Назначение:
// Представить результат моделирования в виде табличной модели Qt Model/View.
//
// Что делает класс:
// - хранит указатель на SimulationResult;
// - отдает число строк и столбцов таблицы;
// - преобразует значения из SimulationSample в строки для отображения;
// - формирует заголовки столбцов.
//
// final означает, что от этой модели дальше не предполагается наследование.
// ----------------------------------------------------------------------------
class SimulationTableModel final : public QAbstractTableModel
{
public:
    // ----------------------------------------------------------------------------
    // КОНСТРУКТОР SimulationTableModel
    // ----------------------------------------------------------------------------
    // Назначение:
    // Создать объект табличной модели результатов.
    //
    // Принимает:
    // - parent - родительский QObject.
    // ----------------------------------------------------------------------------
    // Конструктор табличной модели.
    //
    // QObject* parent - стандартный родительский объект Qt.
    explicit SimulationTableModel(QObject* parent = nullptr) : QAbstractTableModel(parent)
    {
    }

    // ----------------------------------------------------------------------------
    // МЕТОД SetResult
    // ----------------------------------------------------------------------------
    // Назначение:
    // Передать модели новый результат моделирования, который затем будет
    // отображаться в таблице.
    // ----------------------------------------------------------------------------
    void SetResult(const SimulationResult* result)
    {
        // beginResetModel() / endResetModel() - стандартная пара методов Qt
        // Model/View, которая сообщает представлению, что модель будет
        // полностью пересобрана.
        beginResetModel();
        result_ = result;
        endResetModel();
    }

    // ----------------------------------------------------------------------------
    // МЕТОД rowCount
    // ----------------------------------------------------------------------------
    // Назначение:
    // Вернуть количество строк, которое должна показывать таблица.
    //
    // Принимает:
    // - parent - родительский индекс модели.
    //
    // Возвращает:
    // - число строк таблицы.
    // ----------------------------------------------------------------------------
    int rowCount(const QModelIndex& parent = QModelIndex()) const override
    {
        // Если parent валиден, значит Qt спрашивает число "дочерних" строк
        // у некоторого вложенного элемента. Наша модель плоская, поэтому
        // в таком случае возвращаем 0.
        //
        // Если result_ не задан, строк тоже нет.
        if (parent.isValid() || result_ == nullptr)
        {
            return 0;
        }

        // size() возвращает число сохраненных выборок траектории.
        // static_cast<int>(...) нужен, потому что size() возвращает size_t.
        return static_cast<int>(result_->samples.size());
    }

    // ----------------------------------------------------------------------------
    // МЕТОД columnCount
    // ----------------------------------------------------------------------------
    // Назначение:
    // Вернуть количество столбцов таблицы.
    //
    // Принимает:
    // - parent - родительский индекс модели.
    //
    // Возвращает:
    // - число столбцов таблицы.
    // ----------------------------------------------------------------------------
    int columnCount(const QModelIndex& parent = QModelIndex()) const override
    {
        // Таблица также плоская: у дочерних узлов столбцов нет.
        // Для корневого уровня всегда возвращаем 9 фиксированных столбцов.
        return parent.isValid() ? 0 : 9;
    }

    // ----------------------------------------------------------------------------
    // МЕТОД data
    // ----------------------------------------------------------------------------
    // Назначение:
    // Вернуть данные конкретной ячейки таблицы или служебные данные
    // отображения для этой ячейки.
    //
    // Принимает:
    // - index - адрес ячейки модели;
    // - role  - тип запрашиваемых данных.
    //
    // Возвращает:
    // - QVariant с текстом, выравниванием или пустым значением.
    // ----------------------------------------------------------------------------
    QVariant data(const QModelIndex& index, int role = Qt::DisplayRole) const override
    {
        // QModelIndex описывает адрес ячейки в модели.
        // Если индекс некорректен, результата нет или номер строки выходит
        // за границы, возвращаем пустой QVariant.
        if (!index.isValid() || result_ == nullptr || index.row() < 0 ||
            index.row() >= static_cast<int>(result_->samples.size()))
        {
            return {};
        }

        // Получаем выборку, соответствующую строке таблицы.
        const SimulationSample& sample = result_->samples[static_cast<std::size_t>(index.row())];

        // Role в Qt Model/View определяет, какую именно информацию запросило
        // представление: текст, выравнивание, цвет и т.д.
        if (role == Qt::TextAlignmentRole)
        {
            // Для TextAlignmentRole возвращаем центрирование содержимого.
            return Qt::AlignCenter;
        }

        // Если запрошена не текстовая роль отображения, ничего не возвращаем.
        if (role != Qt::DisplayRole)
        {
            return {};
        }

        // switch(...) выбирает, что показывать в зависимости от номера столбца.
        switch (index.column())
        {
        case 0:
            // Столбец времени t.
            return FormatNumericValue(sample.time);
        case 1:
            // Координата x.
            return FormatNumericValue(sample.state[0]);
        case 2:
            // Координата y.
            return FormatNumericValue(sample.state[1]);
        case 3:
            // Координата z.
            return FormatNumericValue(sample.state[2]);
        case 4:
            // Скорость Vx.
            return FormatNumericValue(sample.state[3]);
        case 5:
            // Скорость Vy.
            return FormatNumericValue(sample.state[4]);
        case 6:
            // Скорость Vz.
            return FormatNumericValue(sample.state[5]);
        case 7:
            // Модуль радиус-вектора.
            return FormatNumericValue(Radius(sample.state));
        case 8:
            // Модуль скорости.
            return FormatNumericValue(Speed(sample.state));
        default:
            // Для несуществующих столбцов возвращаем пустое значение.
            return {};
        }
    }

    // ----------------------------------------------------------------------------
    // МЕТОД headerData
    // ----------------------------------------------------------------------------
    // Назначение:
    // Вернуть подписи заголовков таблицы.
    //
    // Принимает:
    // - section     - номер секции заголовка;
    // - orientation - ориентация заголовка;
    // - role        - тип запрашиваемых данных.
    //
    // Возвращает:
    // - QVariant с подписью заголовка, выравниванием или значением
    //   базового класса.
    // ----------------------------------------------------------------------------
    QVariant headerData(int section, Qt::Orientation orientation, int role = Qt::DisplayRole) const override
    {
        if (role == Qt::TextAlignmentRole)
        {
            // Заголовки тоже центрируем.
            return Qt::AlignCenter;
        }

        // Для вертикальных заголовков или для других ролей делегируем
        // поведение базовому классу.
        if (role != Qt::DisplayRole || orientation != Qt::Horizontal)
        {
            return QAbstractTableModel::headerData(section, orientation, role);
        }

        // QStringList - это Qt-контейнер списка строк.
        // Здесь он хранит подписи всех горизонтальных столбцов таблицы.
        static const QStringList kHeaders = {
            "t, c",
            "x, м",
            "y, м",
            "z, м",
            "V<sub>x</sub>, м/с",
            "V<sub>y</sub>, м/с",
            "V<sub>z</sub>, м/с",
            "r, м",
            "|V|, м/с"};

        // Защита от некорректного индекса секции.
        if (section < 0 || section >= kHeaders.size())
        {
            return {};
        }

        // Возвращаем строку заголовка по номеру столбца.
        return kHeaders[section];
    }

private:
    // Указатель на текущий результат моделирования, который отображает модель.
    const SimulationResult* result_ = nullptr;
};

// ----------------------------------------------------------------------------
// ФУНКЦИЯ FullTableWidth
// ----------------------------------------------------------------------------
// Назначение:
// Вычислить полную ширину таблицы вместе с заголовками и рамкой.
//
// Принимает:
// - table - указатель на таблицу.
// ----------------------------------------------------------------------------
int FullTableWidth(const QTableView* table)
{
    // Ширина складывается из:
    // - ширины вертикального заголовка;
    // - суммарной длины горизонтального заголовка;
    // - толщины двух сторон рамки.
    return table->verticalHeader()->width() + table->horizontalHeader()->length() + 2 * table->frameWidth();
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ FullTableHeight
// ----------------------------------------------------------------------------
// Назначение:
// Вычислить полную высоту таблицы вместе с заголовками и рамкой.
//
// Принимает:
// - table - указатель на таблицу.
// ----------------------------------------------------------------------------
int FullTableHeight(const QTableView* table)
{
    // Высота складывается из:
    // - высоты горизонтального заголовка;
    // - суммарной длины вертикального заголовка;
    // - толщины двух сторон рамки.
    return table->horizontalHeader()->height() + table->verticalHeader()->length() + 2 * table->frameWidth();
}

