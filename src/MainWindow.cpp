// ----------------------------------------------------------------------------
// МЕТОД SetupSignals
// ----------------------------------------------------------------------------
// Назначение:
// Связать сигналы пользовательского интерфейса с обработчиками MainWindow.
//
// Что делает метод:
// 1) подключает кнопку запуска моделирования;
// 2) подключает кнопку экспорта таблицы;
// 3) обновляет графики при переключении вкладок результатов;
// 4) следит за изменением всех числовых полей и пересчитывает доступность
//    кнопки запуска.
// ----------------------------------------------------------------------------
void MainWindow::SetupSignals()
{
    // connect(...) связывает сигнал clicked кнопки запуска
    // с методом RunSimulation().
    connect(runButton_, &QPushButton::clicked, this, &MainWindow::RunSimulation);

    // Аналогично связываем кнопку экспорта с методом ExportTablePng().
    connect(exportTableButton_, &QPushButton::clicked, this, &MainWindow::ExportTablePng);

    // currentChanged срабатывает при переключении вкладки результатов.
    // Здесь используется лямбда, потому что нужно сначала проверить,
    // есть ли вообще рассчитанный результат.
    connect(resultsTabs_, &QTabWidget::currentChanged, this, [this](int) {
        // Если результата еще нет, обновлять графики нечем.
        if (hasResult_)
        {
            // Обновляем только ту группу результатов, которая сейчас видима.
            RefreshVisibleResults();
        }
    });

    // Собираем все числовые поля ввода в один массив, чтобы не писать
    // одинаковый connect вручную для каждого поля по отдельности.
    const std::array<QDoubleSpinBox*, 9> fields = {
        xEdit_, yEdit_, zEdit_, vxEdit_, vyEdit_, vzEdit_, t0Edit_, tkEdit_, hEdit_};

    // Проходим по всем полям и подписываемся на их изменения.
    for (auto* field : fields)
    {
        // valueChanged приходит, когда значение спинбокса меняется как число,
        // например через кнопки +/-, колесо мыши или программную установку.
        connect(field, &QDoubleSpinBox::valueChanged, this, [this](double) { RefreshRunButtonState(); });

        // Внутри QDoubleSpinBox есть QLineEdit. Он нужен, чтобы отлавливать
        // и "сырой" текстовый ввод пользователя, когда число еще только
        // набирается и не обязательно уже стало валидным значением.
        if (auto* edit = field->findChild<QLineEdit*>())
        {
            // textChanged позволяет сразу реагировать на очистку поля,
            // ввод знака минус, точки и другие промежуточные состояния.
            connect(edit, &QLineEdit::textChanged, this, [this](const QString&) { RefreshRunButtonState(); });
        }
    }

    // Один раз синхронизируем состояние кнопки запуска после построения UI.
    RefreshRunButtonState();
}

// ----------------------------------------------------------------------------
// МЕТОД ResetOutputs
// ----------------------------------------------------------------------------
// Назначение:
// Полностью очистить предыдущие результаты моделирования в UI.
//
// Что делает метод:
// 1) сбрасывает текст итоговой сводки;
// 2) очищает таблицу результатов;
// 3) отключает экспорт;
// 4) очищает все графики;
// 5) сбрасывает внутренние флаги и последний сохраненный результат.
// ----------------------------------------------------------------------------
void MainWindow::ResetOutputs()
{
    // Возвращаем текстовые поля итоговой сводки в пустое состояние.
    samplesValue_->setText("—");
    finalTimeValue_->setText("—");
    radiusValue_->setText("—");
    speedValue_->setText("—");

    // Передаем модели таблицы nullptr, чтобы она считала, что данных нет.
    static_cast<SimulationTableModel*>(samplesModel_)->SetResult(nullptr);

    // Пока результатов нет, экспорт PNG должен быть недоступен.
    exportTableButton_->setEnabled(false);

    // Сбрасываем внутренний признак наличия результата.
    hasResult_ = false;

    // Затираем сохраненный результат пустой структурой.
    lastResult_ = SimulationResult{};

    // Очищаем все графики координат.
    for (auto* plot : coordinatePlots_)
    {
        plot->Clear();
    }

    // Очищаем все графики скоростей.
    for (auto* plot : velocityPlots_)
    {
        plot->Clear();
    }

    // Очищаем все графики траекторий.
    for (auto* plot : trajectoryPlots_)
    {
        plot->Clear();
    }
}

// ----------------------------------------------------------------------------
// МЕТОД RunSimulation
// ----------------------------------------------------------------------------
// Назначение:
// Выполнить полный цикл моделирования: от чтения параметров интерфейса
// до показа таблицы, сводки и графиков.
//
// Что делает метод:
// 1) очищает предыдущий результат;
// 2) проверяет корректность ввода;
// 3) создает выбранный интегратор;
// 4) задает модель движения и начальное состояние;
// 5) запускает численное интегрирование;
// 6) показывает результат либо сообщение об ошибке.
// ----------------------------------------------------------------------------
void MainWindow::RunSimulation()
{
    // Перед новым расчетом обязательно очищаем старые данные из интерфейса.
    ResetOutputs();

    // Сюда будет записано текстовое описание ошибки валидации, если она есть.
    QString errorMessage;

    // Если пользователь ввел некорректные данные, расчет запускать нельзя.
    if (!ValidateInput(errorMessage))
    {
        ShowStatus(errorMessage, false);
        return;
    }

    // Указатель на интегратор объявляем заранее, чтобы в случае исключения
    // его можно было удалить в catch.
    TAbstractIntegrator* integrator = nullptr;

    try
    {
        // Считываем текущие координаты из полей интерфейса.
        const double x = xEdit_->value();
        const double y = yEdit_->value();
        const double z = zEdit_->value();

        // Считываем текущие компоненты скорости.
        const double vx = vxEdit_->value();
        const double vy = vyEdit_->value();
        const double vz = vzEdit_->value();

        // Считываем параметры интегрирования.
        const double t0 = t0Edit_->value();
        const double tk = tkEdit_->value();
        const double h = hEdit_->value();

        // currentIndex() возвращает номер выбранного метода в комбобоксе.
        const int methodIndex = methodComboBox_->currentIndex();

        // Защитная проверка: индекс должен попадать в диапазон массива
        // integratorOptions_.
        if (methodIndex < 0 || methodIndex >= static_cast<int>(integratorOptions_.size()))
        {
            ShowStatus("Выбран некорректный метод интегрирования.", false);
            return;
        }

        // Фабрика выбранного варианта создает конкретный объект интегратора:
        // либо Эйлера, либо Рунге-Кутты 4 порядка.
        integrator = integratorOptions_[static_cast<std::size_t>(methodIndex)].factory(t0, tk, h);

        // Если фабрика по какой-то причине вернула nullptr, продолжать нельзя.
        if (integrator == nullptr)
        {
            ShowStatus("Не удалось создать выбранный интегратор.", false);
            return;
        }

        // Передаем интегратору модель правых частей движения КА.
        integrator->SetRightParts(spaceCraftModel_);

        // Устанавливаем начальный вектор состояния:
        // x, y, z, Vx, Vy, Vz.
        integrator->SetInitialState({x, y, z, vx, vy, vz});

        // Запускаем интегрирование до терминального момента времени tk.
        const SimulationResult result = integrator->MoveTo(tk);

        // Интегратор больше не нужен, поэтому удаляем его сразу после расчета.
        delete integrator;
        integrator = nullptr;

        // Если интегратор сообщил неуспех и при этом не вернул ни одной точки,
        // пользователю показываем только сообщение об ошибке.
        if (!result.success && result.samples.empty())
        {
            ShowStatus(QString::fromStdString(result.message), false);
            return;
        }

        // Сохраняем результат в поле класса для дальнейшего использования
        // таблицей, графиками и экспортом.
        lastResult_ = result;

        // Помечаем, что валидный результат уже существует.
        hasResult_ = true;

        // Заполняем табличное представление.
        PopulateTable(result);

        // Обновляем итоговую сводку по количеству точек, времени, радиусу и скорости.
        UpdateSummary(result);

        // Обновляем текущую видимую вкладку результатов.
        RefreshVisibleResults();

        // Дополнительное отложенное обновление через очередь событий Qt.
        // Оно нужно, чтобы графики корректно появились даже если вкладка
        // только что была очищена и layout еще не завершил перестройку.
        QTimer::singleShot(0, this, [this]() {
            if (hasResult_)
            {
                RefreshVisibleResults();
            }
        });

        // Экспорт разрешаем только если есть хотя бы одна рассчитанная точка.
        exportTableButton_->setEnabled(!result.samples.empty());

        // success=true означает, что расчет завершился штатно.
        if (result.success)
        {
            ShowStatus("Моделирование завершено успешно. Данные готовы для построения графиков.", true);
        }

        // recoverable-результат означает, что расчет мог остановиться раньше
        // нормы, но уже накопленные данные все равно полезны пользователю.
        else if (IsRecoverableResult(result))
        {
            ShowStatus(QString::fromStdString(result.message), false);
        }

        // Все остальные неуспешные исходы также показываем через статус.
        else
        {
            ShowStatus(QString::fromStdString(result.message), false);
        }
    }

    // std::exception - базовый класс стандартных исключений C++.
    // Сюда попадают ошибки валидации, вычислений, создания объектов и т.д.
    catch (const std::exception& ex)
    {
        // Если исключение произошло до ручного delete, очищаем интегратор здесь.
        delete integrator;

        // what() возвращает C-строку с описанием ошибки. fromUtf8(...)
        // переводит ее в QString для показа в интерфейсе.
        ShowStatus(QString("Ошибка: %1").arg(QString::fromUtf8(ex.what())), false);
    }
}

// ----------------------------------------------------------------------------
// МЕТОД ValidateInput
// ----------------------------------------------------------------------------
// Назначение:
// Проверить корректность всех введенных пользователем параметров перед
// запуском моделирования.
//
// Принимает:
// - errorMessage - строку, в которую при ошибке записывается понятное
//   объяснение причины отказа.
//
// Возвращает:
// - true, если все поля заполнены корректно;
// - false, если найдена хотя бы одна ошибка ввода.
// ----------------------------------------------------------------------------
bool MainWindow::ValidateInput(QString& errorMessage) const
{
    // std::array - контейнер фиксированного размера.
    // std::pair<QString, const QDoubleSpinBox*> хранит пару:
    // 1) человекочитаемое имя поля;
    // 2) указатель на само поле ввода.
    //
    // Такой массив позволяет единообразно пройти по всем полям и не писать
    // девять почти одинаковых проверок вручную.
    const std::array<std::pair<QString, const QDoubleSpinBox*>, 9> fields = {{
        {"x0", xEdit_},
        {"y0", yEdit_},
        {"z0", zEdit_},
        {"Vx0", vxEdit_},
        {"Vy0", vyEdit_},
        {"Vz0", vzEdit_},
        {"t0", t0Edit_},
        {"tk", tkEdit_},
        {"h", hEdit_},
    }};

    // Последовательно проверяем каждое поле.
    for (const auto& field : fields)
    {
        // HasSpinBoxInput(...) проверяет, что пользователь реально оставил
        // в поле какой-то ввод, а не пустую строку.
        if (!HasSpinBoxInput(field.second))
        {
            // QString::arg(...) подставляет имя конкретного поля в шаблон.
            errorMessage = QString("Поле %1 не должно быть пустым.").arg(field.first);
            return false;
        }

        // value() возвращает текущее числовое значение спинбокса.
        const double value = field.second->value();

        // std::isfinite(...) отбрасывает NaN и бесконечности.
        if (!std::isfinite(value))
        {
            errorMessage = QString("Поле %1 должно содержать корректное число.").arg(field.first);
            return false;
        }
    }

    // После общей проверки читаем конкретные параметры, которые участвуют
    // уже в смысловых ограничениях задачи.
    const double t0 = t0Edit_->value();
    const double tk = tkEdit_->value();
    const double h = hEdit_->value();
    const double x = xEdit_->value();
    const double y = yEdit_->value();
    const double z = zEdit_->value();

    // Терминальный момент должен быть строго больше начального.
    if (tk <= t0)
    {
        errorMessage = "Должно выполняться tk > t0.";
        return false;
    }

    // Шаг интегрирования должен быть положительным.
    if (h <= 0.0)
    {
        errorMessage = "Шаг интегрирования h должен быть больше нуля.";
        return false;
    }

    // Защитное ограничение на число шагов.
    // Если шагов будет слишком много, приложение может начать заметно
    // тормозить или потреблять слишком много памяти.
    if (((tk - t0) / h) > 500000.0)
    {
        errorMessage = "Слишком много шагов интегрирования. Увеличь h или уменьши tk.";
        return false;
    }

    // Если x = y = z = 0, начальный радиус-вектор нулевой, а в модели
    // центрального поля это приводит к физически и численно некорректной
    // постановке задачи.
    if (std::abs(x) + std::abs(y) + std::abs(z) < 1.0)
    {
        errorMessage = "Начальный радиус-вектор не должен быть нулевым.";
        return false;
    }

    // Если ни одна проверка не сработала, ввод считаем корректным.
    return true;
}

// ----------------------------------------------------------------------------
// МЕТОД RefreshRunButtonState
// ----------------------------------------------------------------------------
// Назначение:
// Пересчитать, должна ли кнопка запуска моделирования быть активной.
//
// Что делает метод:
// 1) проверяет заполненность всех обязательных полей;
// 2) включает кнопку, если заполнены все поля;
// 3) отключает кнопку, если хотя бы одно поле пустое.
// ----------------------------------------------------------------------------
void MainWindow::RefreshRunButtonState()
{
    // Собираем все обязательные поля в один фиксированный массив.
    const std::array<QDoubleSpinBox*, 9> fields = {
        xEdit_, yEdit_, zEdit_, vxEdit_, vyEdit_, vzEdit_, t0Edit_, tkEdit_, hEdit_};

    // std::all_of(...) возвращает true только если предикат выполняется
    // для каждого элемента диапазона.
    const bool allFilled = std::all_of(fields.begin(), fields.end(), [](const QDoubleSpinBox* field) {
        // Для каждого поля проверяем, что в нем есть пользовательский ввод.
        return HasSpinBoxInput(field);
    });

    // Дополнительная защита: кнопка может быть еще не создана на очень ранней
    // стадии жизни окна.
    if (runButton_ != nullptr)
    {
        // setEnabled(true/false) включает или отключает кнопку.
        runButton_->setEnabled(allFilled);
    }
}

// ----------------------------------------------------------------------------
// МЕТОД PopulateTable
// ----------------------------------------------------------------------------
// Назначение:
// Передать рассчитанный результат табличной модели и обновить таблицу.
//
// Принимает:
// - result - результат моделирования, который логически нужно показать
//   в таблице. Фактически модель получает уже сохраненный lastResult_.
// ----------------------------------------------------------------------------
void MainWindow::PopulateTable(const SimulationResult& result)
{
    // Временно запрещаем визуальные обновления таблицы, чтобы избежать
    // лишней перерисовки в процессе замены данных.
    samplesTable_->setUpdatesEnabled(false);

    // result здесь явно не используется напрямую, потому что модель
    // привязывается к полю lastResult_ класса MainWindow.
    Q_UNUSED(result);

    // Передаем модели адрес последнего сохраненного результата.
    static_cast<SimulationTableModel*>(samplesModel_)->SetResult(&lastResult_);

    // Снова разрешаем обновления таблицы.
    samplesTable_->setUpdatesEnabled(true);
}

// ----------------------------------------------------------------------------
// МЕТОД ExportTablePng
// ----------------------------------------------------------------------------
// Назначение:
// Экспортировать текущую таблицу траектории в PNG-файл.
//
// Что делает метод:
// 1) проверяет, есть ли рассчитанные данные;
// 2) собирает временную "карточку" с заголовком и таблицей;
// 3) рендерит эту карточку в QImage;
// 4) сохраняет изображение в каталог exports/tables;
// 5) показывает пользователю итоговый статус.
// ----------------------------------------------------------------------------
void MainWindow::ExportTablePng()
{
    // Если результатов еще нет, экспортировать нечего.
    if (lastResult_.samples.empty())
    {
        ShowStatus("Таблица пока пуста. Сначала выполните моделирование.", false);
        return;
    }

    // Создаем временный виджет-карточку, который будет использоваться
    // только как "сцена" для красивого рендера таблицы в изображение.
    QWidget exportCard;

    // Назначаем тот же objectName, что и у обычной карточки результатов,
    // чтобы к этому временному виджету применились те же QSS-правила.
    exportCard.setObjectName("resultsCard");

    // Копируем stylesheet главного окна, чтобы экспорт выглядел так же,
    // как и таблица в реальном интерфейсе.
    exportCard.setStyleSheet(styleSheet());

    // Qt::WA_DontShowOnScreen означает: виджет можно полировать и рендерить,
    // но не нужно реально показывать его пользователю на экране.
    exportCard.setAttribute(Qt::WA_DontShowOnScreen, true);

    // Основной вертикальный layout экспортной карточки.
    auto* exportLayout = new QVBoxLayout(&exportCard);
    exportLayout->setContentsMargins(18, 18, 18, 18);
    exportLayout->setSpacing(10);

    // Заголовок экспортируемой таблицы.
    auto* exportTitle = new QLabel("Таблица траектории", &exportCard);
    exportTitle->setObjectName("tableTitle");
    exportLayout->addWidget(exportTitle);

    // Создаем отдельную таблицу специально для экспорта, чтобы не зависеть
    // от текущего состояния видимой таблицы на экране.
    auto* exportTable = new QTableView(&exportCard);

    // Отдельная модель данных для экспортной таблицы.
    auto* exportModel = new SimulationTableModel(exportTable);

    // Передаем модели рассчитанный результат.
    exportModel->SetResult(&lastResult_);
    exportTable->setModel(exportModel);

    // Ставим тот же rich-text заголовок, что и в основной таблице.
    exportTable->setHorizontalHeader(new RichTextHeaderView(Qt::Horizontal, exportTable));

    // Копируем ключевые режимы поведения из основной таблицы интерфейса.
    exportTable->setSelectionBehavior(samplesTable_->selectionBehavior());
    exportTable->setSelectionMode(samplesTable_->selectionMode());
    exportTable->setEditTriggers(QAbstractItemView::NoEditTriggers);
    exportTable->setAlternatingRowColors(samplesTable_->alternatingRowColors());
    exportTable->setWordWrap(samplesTable_->wordWrap());
    exportTable->setTextElideMode(samplesTable_->textElideMode());

    // Скроллбары в экспортной версии не нужны: размер виджета будет подобран
    // так, чтобы вся таблица поместилась целиком.
    exportTable->setVerticalScrollBarPolicy(Qt::ScrollBarAlwaysOff);
    exportTable->setHorizontalScrollBarPolicy(Qt::ScrollBarAlwaysOff);

    // Переносим важные настройки заголовков.
    exportTable->verticalHeader()->setVisible(samplesTable_->verticalHeader()->isVisible());
    exportTable->horizontalHeader()->setStretchLastSection(samplesTable_->horizontalHeader()->stretchLastSection());
    exportTable->horizontalHeader()->setMinimumSectionSize(samplesTable_->horizontalHeader()->minimumSectionSize());
    exportTable->horizontalHeader()->setDefaultAlignment(Qt::AlignCenter);

    // Для каждого столбца переносим ширину и режим resize так, чтобы экспорт
    // визуально совпадал с основной таблицей.
    for (int column = 0; column < exportModel->columnCount(); ++column)
    {
        exportTable->setColumnWidth(column, samplesTable_->columnWidth(column));
        exportTable->horizontalHeader()->setSectionResizeMode(
            column, samplesTable_->horizontalHeader()->sectionResizeMode(column));
    }

    // Вычисляем полный требуемый размер таблицы.
    // std::max(..., 900) и std::max(..., 280) не дают экспортируемой карточке
    // стать слишком маленькой даже для коротких результатов.
    const int exportWidth = std::max(FullTableWidth(exportTable), 900);
    const int exportHeight = std::max(FullTableHeight(exportTable), 280);

    // Фиксируем размер таблицы, чтобы рендер дал именно нужную геометрию.
    exportTable->setMinimumSize(exportWidth, exportHeight);
    exportTable->setMaximumSize(exportWidth, exportHeight);
    exportLayout->addWidget(exportTable);

    // ensurePolished() принудительно применяет стили и другие "полировочные"
    // действия Qt до фактического рендера в картинку.
    exportCard.ensurePolished();

    // activate() заставляет layout пересчитать геометрию дочерних элементов.
    exportLayout->activate();

    // Добавляем внешние отступы карточки и место под заголовок.
    const int finalWidth = exportWidth + 36;
    const int finalHeight = exportHeight + std::max(exportTitle->sizeHint().height(), 32) + 46;

    // Устанавливаем окончательный размер экспортной карточки.
    exportCard.resize(finalWidth, finalHeight);

    // Явно задаем geometry layout по всей карточке.
    exportLayout->setGeometry(QRect(0, 0, finalWidth, finalHeight));
    exportLayout->activate();

    // Создаем изображение нужного размера.
    // Format_ARGB32_Premultiplied - один из стандартных форматов Qt для рендера
    // с поддержкой альфа-канала.
    QImage image(QSize(finalWidth, finalHeight), QImage::Format_ARGB32_Premultiplied);

    // Заполняем фон изображения белым цветом.
    image.fill(Qt::white);

    // QPainter будет рисовать содержимое exportCard в объект image.
    QPainter painter(&image);
    exportCard.render(&painter);
    painter.end();

    // Формируем полный путь к будущему PNG-файлу.
    const QString filePath = QDir(ProjectTablesDirectoryPath()).absoluteFilePath(BuildTableExportFileName());

    // Если сохранение не удалось, показываем ошибку и завершаем метод.
    if (!image.save(filePath, "PNG"))
    {
        ShowStatus("Не удалось сохранить PNG таблицы.", false);
        return;
    }

    // Сообщаем пользователю, куда именно был сохранен PNG.
    ShowStatus(QString("PNG таблицы сохранён: %1").arg(filePath), true);
}

// ----------------------------------------------------------------------------
// МЕТОД UpdateSummary
// ----------------------------------------------------------------------------
// Назначение:
// Обновить текстовую сводку по результату моделирования.
//
// Принимает:
// - result - рассчитанный результат моделирования.
// ----------------------------------------------------------------------------
void MainWindow::UpdateSummary(const SimulationResult& result)
{
    // Если точек нет, сводку заполнять нечем.
    if (result.samples.empty())
    {
        return;
    }

    // back() возвращает последнюю точку траектории, то есть финальное состояние.
    const auto& lastSample = result.samples.back();

    // qulonglong используется, чтобы безопасно вывести размер контейнера Qt-строкой.
    samplesValue_->setText(QString::number(static_cast<qulonglong>(result.samples.size())));

    // Заполняем финальное время, радиус и скорость в удобном текстовом формате.
    finalTimeValue_->setText(QString("%1 c").arg(FormatNumber(lastSample.time)));
    radiusValue_->setText(QString("%1 м").arg(FormatNumber(Radius(lastSample.state))));
    speedValue_->setText(QString("%1 м/с").arg(FormatNumber(Speed(lastSample.state))));
}

// ----------------------------------------------------------------------------
// МЕТОД UpdatePlots
// ----------------------------------------------------------------------------
// Назначение:
// Подготовить серии точек и передать их на графики в зависимости от
// активной вкладки результатов.
//
// Принимает:
// - result - результат моделирования, по которому нужно построить графики.
// ----------------------------------------------------------------------------
void MainWindow::UpdatePlots(const SimulationResult& result)
{
    // Если данных нет, строить графики нечего.
    if (result.samples.empty())
    {
        return;
    }

    // QVector<QPointF> - это контейнер Qt для набора двумерных точек.
    // Именно в таком виде PlotChartWidget ожидает входные данные.
    //
    // Для координатных графиков по оси X откладывается время t,
    // а по оси Y - соответствующая координата.
    QVector<QPointF> xSeries;
    QVector<QPointF> ySeries;
    QVector<QPointF> zSeries;

    // Для графиков скоростей по оси X также идет время t,
    // а по оси Y - компонента скорости.
    QVector<QPointF> vxSeries;
    QVector<QPointF> vySeries;
    QVector<QPointF> vzSeries;

    // Для пространственных проекций обе координаты берутся уже не из времени,
    // а из самого вектора состояния.
    QVector<QPointF> xySeries;
    QVector<QPointF> yzSeries;
    QVector<QPointF> xzSeries;

    // Количество сэмплов заранее известно, поэтому полезно сразу зарезервировать
    // память под все векторы, чтобы уменьшить число реаллокаций.
    const int count = static_cast<int>(result.samples.size());
    xSeries.reserve(count);
    ySeries.reserve(count);
    zSeries.reserve(count);
    vxSeries.reserve(count);
    vySeries.reserve(count);
    vzSeries.reserve(count);
    xySeries.reserve(count);
    yzSeries.reserve(count);
    xzSeries.reserve(count);

    // Последовательно разворачиваем каждый SimulationSample в несколько
    // различных представлений для разных графиков.
    for (const auto& sample : result.samples)
    {
        // sample.time - момент времени данного сэмпла.
        // sample.state[0], [1], [2] - это x, y, z.
        //
        // append(...) добавляет новую точку в конец соответствующего QVector.
        // Графики координат во времени.
        xSeries.append(QPointF(sample.time, sample.state[0]));
        ySeries.append(QPointF(sample.time, sample.state[1]));
        zSeries.append(QPointF(sample.time, sample.state[2]));

        // sample.state[3], [4], [5] - это Vx, Vy, Vz.
        // Графики скоростей во времени.
        vxSeries.append(QPointF(sample.time, sample.state[3]));
        vySeries.append(QPointF(sample.time, sample.state[4]));
        vzSeries.append(QPointF(sample.time, sample.state[5]));

        // Здесь время уже не используется.
        // Для XY берутся координаты (x, y), для YZ - (y, z), для XZ - (x, z).
        // Пространственные проекции траектории.
        xySeries.append(QPointF(sample.state[0], sample.state[1]));
        yzSeries.append(QPointF(sample.state[1], sample.state[2]));
        xzSeries.append(QPointF(sample.state[0], sample.state[2]));
    }

    // Определяем, закончился ли расчет остановкой по столкновению.
    // contains(..., Qt::CaseInsensitive) ищет слово "столкновение" без учета регистра.
    const bool hasCollisionStop = (!result.success && !result.samples.empty() &&
                                   QString::fromStdString(result.message).contains("столкновение", Qt::CaseInsensitive));

    // Момент остановки - это время последнего сохраненного сэмпла.
    const double stopTime = result.samples.back().time;

    // Подпись маркера события на графиках координат.
    const QString stopLabel = QString::fromUtf8("|r| = Rземли");

    // Строим только ту группу графиков, которая сейчас видима пользователю.
    // currentIndex() возвращает индекс активной вкладки resultsTabs_:
    // 0 - координаты, 1 - скорости, 2 - траектории, 3 - таблица.
    //
    // Если resultsTabs_ по какой-то причине еще не создан, берем 0 как
    // безопасное значение по умолчанию.
    const int tabIndex = resultsTabs_ != nullptr ? resultsTabs_->currentIndex() : 0;
    if (tabIndex == 0)
    {
        // Вкладка координат: передаем графикам серии X(t), Y(t), Z(t).
        coordinatePlots_[0]->SetData(xSeries);
        coordinatePlots_[1]->SetData(ySeries);
        coordinatePlots_[2]->SetData(zSeries);

        // На координатных графиках при столкновении показываем вертикальный маркер.
        for (auto* plot : coordinatePlots_)
        {
            if (hasCollisionStop)
            {
                // SetEventMarker(...) рисует вертикальную отметку по времени stopTime
                // и подпись рядом с ней.
                plot->SetEventMarker(stopTime, stopLabel);
            }
            else
            {
                // Если столкновения не было, следы старого маркера нужно убрать.
                plot->ClearEventMarker();
            }
        }
    }
    else if (tabIndex == 1)
    {
        // Вкладка скоростей: передаем графикам серии Vx(t), Vy(t), Vz(t).
        velocityPlots_[0]->SetData(vxSeries);
        velocityPlots_[1]->SetData(vySeries);
        velocityPlots_[2]->SetData(vzSeries);

        // На скоростях специальные маркеры события не показываем.
        for (auto* plot : velocityPlots_)
        {
            // На случай, если раньше был открыт другой тип графиков,
            // очищаем любые старые отметки.
            plot->ClearEventMarker();
        }
    }
    else if (tabIndex == 2)
    {
        // Вкладка траекторий: передаем пространственные проекции XY, YZ, XZ.
        trajectoryPlots_[0]->SetData(xySeries);
        trajectoryPlots_[1]->SetData(yzSeries);
        trajectoryPlots_[2]->SetData(xzSeries);

        // На траекториях маркеры столкновения сейчас отключены.
        for (auto* plot : trajectoryPlots_)
        {
            // Поэтому просто гарантированно очищаем возможные старые маркеры.
            plot->ClearEventMarker();
        }
    }
}

// ----------------------------------------------------------------------------
// МЕТОД RefreshVisibleResults
// ----------------------------------------------------------------------------
// Назначение:
// Обновить только те результаты, которые сейчас должны быть видимы пользователю.
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
// МЕТОД RefreshVisibleResults
// ----------------------------------------------------------------------------
// Назначение:
// Обновить ту часть результатов, которая сейчас должна быть видна пользователю.
//
// Что делает метод:
// 1) проверяет, есть ли вообще рассчитанный результат;
// 2) если результат есть, передает его в UpdatePlots(...).
//
// Почему метод называется именно так:
// - графики в приложении обновляются лениво;
// - это значит, что в каждый момент времени мы стараемся перерисовывать
//   только активную вкладку результатов, а не все вкладки сразу.
// ----------------------------------------------------------------------------
void MainWindow::RefreshVisibleResults()
{
    // Если результата еще нет, обновлять нечего.
    if (!hasResult_)
    {
        return;
    }

    // lastResult_ хранит актуальный результат последнего завершенного расчета.
    // UpdatePlots(...) дальше сам разберет, какая именно вкладка сейчас активна
    // и какие графики нужно фактически перестроить.
    UpdatePlots(lastResult_);
}

// ----------------------------------------------------------------------------
// МЕТОД ShowStatus
// ----------------------------------------------------------------------------
// Назначение:
// Показать текстовое сообщение в статусном бейдже и выбрать для него
// цветовую схему успеха или ошибки.
//
// Принимает:
// - text - текст сообщения;
// - success - признак успешного исхода.
// ----------------------------------------------------------------------------
void MainWindow::ShowStatus(const QString& text, bool success)
{
    // statusBadge_ - это QLabel, играющий роль компактного статусного блока.
    // Сначала подставляем в него новый текст.
    // Устанавливаем сам текст сообщения.
    statusBadge_->setText(text);

    // success=true означает "успешный" сценарий, поэтому применяем
    // спокойную зелёную цветовую схему.
    if (success)
    {
        // setStyleSheet(...) здесь применяется локально только к одному QLabel.
        // Поэтому этот стиль не влияет на остальные виджеты окна.
        statusBadge_->setStyleSheet(
            "border: 1px solid #94d7aa; background: #eaf8ef; color: #1d5a32; border-radius: 12px; padding: 10px 12px;");
    }
    else
    {
        // Иначе используем розово-красную схему предупреждения или ошибки.
        // По смыслу сюда попадают и строгие ошибки, и recoverable-сценарии,
        // когда расчет завершился не идеально, но данные все же есть.
        statusBadge_->setStyleSheet(
            "border: 1px solid #efb7bf; background: #fff1f3; color: #7b2636; border-radius: 12px; padding: 10px 12px;");
    }
}

